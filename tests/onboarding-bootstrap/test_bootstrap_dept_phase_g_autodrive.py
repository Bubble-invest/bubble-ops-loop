def test_bootstrap_generates_per_dept_claude_md(bootstrapped_repo: Path) -> None:
    """The bootstrapped repo must contain CLAUDE.md telling the agent to
    auto-drive the SKILL `department-onboarding-guide`."""
    claude_md = bootstrapped_repo / "CLAUDE.md"
    assert claude_md.exists(), "CLAUDE.md must be generated by bootstrap"
    text = claude_md.read_text(encoding="utf-8")
    # Must reference the SKILL by name.
    assert "department-onboarding-guide" in text, \
        "CLAUDE.md must reference the SKILL by name"
    # Must instruct the agent it is autonomous (does not wait).
    assert "autonom" in text.lower(), \
        "CLAUDE.md must declare the agent is autonomous"
    # Must reference Telegram as the ONLY channel.
    assert "telegram" in text.lower(), "CLAUDE.md must mention Telegram"
    # Must mention the per-dept bot handle convention.
    assert "bubbleops" in text.lower(), \
        "CLAUDE.md must mention the @bubbleops<slug>_bot convention"
    # Must enumerate the 7 éclosure steps (FR labels accepted).
    step_aliases = {
        "mandate": ("mandate", "mandat"),
        "missions": ("missions",),
        "layers": ("layers", "layer"),
        "skills": ("skills", "compétences", "competences"),
        "gates": ("gates", "garde-fous", "gate_policies", "garde-fou"),
        "dry-run": ("dry-run", "dry_run", "répétition", "repetition"),
        "activation": ("activation",),
    }
    low = text.lower()
    for canonical, options in step_aliases.items():
        assert any(o in low for o in options), \
            f"CLAUDE.md missing step '{canonical}' (any of: {options})"
    # Must specify the agent's language (English as of scaffold v2).
    assert "english" in low, \
        "CLAUDE.md must specify the agent voice language (english)"



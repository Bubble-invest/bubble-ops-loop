def test_bootstrap_supports_dry_run_mode(scripts_dir: Path, tmp_clone_dir: Path) -> None:
    """--dry-run renders the skeleton to a local tmp dir without invoking gh
    or git push. Used by the Phase G end-to-end smoke."""
    script = scripts_dir / "bootstrap-dept.sh"
    target = tmp_clone_dir / "bubble-ops-drysmoke"
    env = os.environ.copy()
    env["BUBBLE_BOOTSTRAP_CLONE_DIR"] = str(tmp_clone_dir)
    res = subprocess.run(
        [
            "bash", str(script),
            "--slug=drysmoke",
            "--display-name=DrySmoke",
            "--owner=operator",
            "--dry-run",
        ],
        env=env, capture_output=True, text=True,
    )
    assert res.returncode == 0, \
        f"--dry-run must exit 0; stdout={res.stdout}\nstderr={res.stderr}"
    # Skeleton + CLAUDE.md + systemd unit all rendered.
    assert (target / "CLAUDE.md").exists(), \
        f"dry-run must render CLAUDE.md at {target}"
    assert (target / "deploy" / "ops-loop-drysmoke.service").exists(), \
        "dry-run must render the systemd unit"
    assert (target / "dept.yaml.draft").exists(), \
        "dry-run must render dept.yaml.draft"
    assert (target / "onboarding" / "STATE.yaml").exists(), \
        "dry-run must render STATE.yaml"

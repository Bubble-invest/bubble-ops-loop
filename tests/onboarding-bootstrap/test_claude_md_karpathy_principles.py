def test_claude_md_bureau_de_cadre_voice_preserved():
    """The new principles must keep the Bureau-de-Cadre voice ({{OPERATOR}} in 2e
    personne, agent in 1ère personne, French, no English enum slugs)."""
    body = render_claude_md("test-slug", "TestDept")
    # Should NOT contain raw English principle names verbatim (those are
    # Karpathy's English headings; we translated them)
    forbidden_english = [
        "Think Before Coding",
        "Simplicity First",
        "Surgical Changes",
        "Goal-Driven Execution",
    ]
    for english in forbidden_english:
        assert english not in body, (
            f"CLAUDE.md contains English Karpathy heading {english!r} — "
            f"must be translated to French (Bureau-de-Cadre voice)"
        )



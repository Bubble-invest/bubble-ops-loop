"""CLAUDE.md must include the 5 discipline principles (4 Karpathy + 1 bubble-ops).

{{OPERATOR}} directive (msg 2734 + 2737, 2026-05-21): non-tech users will ask our
agents for changes and improvements. Without baked-in discipline, the agents
will scope-creep, refactor unprompted, and accept ambiguous requests as if
they were clear directives. Karpathy's 4 CLAUDE.md principles (from
multica-ai/andrej-karpathy-skills) keep code-helper LLMs on rails — we adapt
them for our dept-running agents and add a 5th specific to bubble-ops:
"Stay in your action class" (never act outside what your gate_policies
authorize, even if asked politely).

The 5 principles must appear in the per-dept CLAUDE.md generated by
scripts/bootstrap-dept.sh, with Bureau-de-Cadre voice (French, 2e personne
for {{OPERATOR}}, 1ère personne for the agent, no jargon).

Reference:
- https://github.com/multica-ai/andrej-karpathy-skills (the 4 source principles)
- scripts/lib/scaffold.py::render_claude_md (the template we extend)
"""
from __future__ import annotations

import sys
from pathlib import Path

# Make scripts/lib/ importable for the test
_PROJECT_ROOT = Path(__file__).resolve().parent.parent.parent
_SCRIPTS_LIB = _PROJECT_ROOT / "scripts" / "lib"
if str(_SCRIPTS_LIB) not in sys.path:
    sys.path.insert(0, str(_SCRIPTS_LIB))

from scaffold import render_claude_md  # noqa: E402


def test_claude_md_contains_principle_1_reflechir_avant_agir():
    """Principle 1 — Think Before Acting (Karpathy 'Think Before Coding')."""
    body = render_claude_md("test-slug", "TestDept")
    # The principle's heading
    assert "Réfléchir avant d'agir" in body or "Réfléchir avant d'agir." in body, (
        "Principle 1 'Réfléchir avant d'agir' missing from CLAUDE.md"
    )
    # The principle's behavior: propose interpretations + ask rather than guess
    assert "interprétation" in body.lower() or "options" in body.lower(), (
        "Principle 1 must mention proposing multiple interpretations / options"
    )
    assert "deviner" in body.lower() or "ambigu" in body.lower(), (
        "Principle 1 must mention not guessing on ambiguity"
    )


